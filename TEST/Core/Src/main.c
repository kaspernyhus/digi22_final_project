/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	char uartBuf[100];
	char lcdBuf[20];
	char logData[100];
	uint8_t lcdToggle = 0;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_I2C1_Init();
  MX_USART3_UART_Init();
  MX_USART1_UART_Init();
  MX_TIM2_Init();
  MX_SPI3_Init();
  /* USER CODE BEGIN 2 */

  HAL_TIM_Base_Start_IT(&htim2);				//Starting Timer2 in interrupt mode
  HAL_UART_Receive_DMA(&huart1, (uint8_t*)rxData, 700);	//Init GPS uart data to DMA

  sprintf(uartBuf, "Boat-log ON!\n");		//Start message on Terminal
  HAL_UART_Transmit(&huart2, (uint8_t*)uartBuf, strlen(uartBuf) , 1000);

  // LCD
  lcd_init ();
  lcd_send_string ("Boat-log ON!");
  HAL_Delay(2000);
  lcd_clear ();

  // BME280
  if (bme280_init(&hi2c1)) {
    sprintf(uartBuf, "BME280 initialized\n");
    HAL_UART_Transmit(&huart2, (uint8_t*)uartBuf, strlen(uartBuf), HAL_MAX_DELAY);
  }

  // ICM20948
  icm20948_init(&hspi3);

  // OpenLog
  //Header for .csv logfile
  sprintf(logData, "Time,Date,Latitude,Longitude,Speed,Course,Temp,Pres,Hum,WaterLvl");
  openlogAppendFile("log1.csv", logData);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  if(gpsFlag == 1)
	  {
		  getLocation(&gps_data, rxData);
		  gps_data.date = rolloverDateConvertion(gps_data.date);
		  gpsFlag = 0;
	  }

	  if(doMeasurement == 1)
	  {
		  doMeasurement = 0;
		  HAL_ADC_Start_IT(&hadc1);
	    bme280_read_all(&bme280_data);

      // Save formatted data to OpenLog
		  sprintf(logData, "%02d:%02d:%02d,%d,%f,%f,%.02f,%.02f,%.02f,%.02f,%.02f,%s",
        gps_data.hours, gps_data.min, gps_data.sec, gps_data.date, gps_data.lati, gps_data.longi, (gps_data.speed*1.852), gps_data.course, bme280_data.temperature, bme280_data.pressure, bme280_data.humidity, waterlevel_str[water_level]);
      openlogAppendFile("log1.csv", logData);
	  }

	  if(waterLevelFlag == 1) {
      waterLevelFlag = 0;
      water_level = check_water_level(waterlevel_reading);
      if (water_level == WATER_LEVEL_HIGH) {
        // Turn on pump
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_13, SET);
      } else {
        // Turn pump off
        HAL_GPIO_WritePin(GPIOB, GPIO_PIN_13, RESET);
      }
	  }

	  if(updateDisp == 1)
	  {
		  updateDisp = 0;
	      if((gps_data.lati != 0) || (gps_data.longi != 0))
		  {
	    	  if (lcdToggle == 0)
			  {
				  sprintf(lcdBuf, "Time: %02d:%02d:%02d", gps_data.hours, gps_data.min , gps_data.sec);
				  lcd_clear ();
				  lcd_put_cur(0, 0);
				  lcd_send_string((char*)lcdBuf);
				  sprintf(lcdBuf, "Date: %d", gps_data.date);
				  lcd_put_cur(1, 0);
				  lcd_send_string((char*)lcdBuf);
				  lcdToggle = 1;
			  }
	    	  else if (lcdToggle == 1)
			  {
				  sprintf(lcdBuf, "Lati: %f", gps_data.lati);
				  lcd_clear ();
				  lcd_put_cur(0, 0);
				  lcd_send_string((char*)lcdBuf);
				  sprintf(lcdBuf, "Long: %f", gps_data.longi);
				  lcd_put_cur(1, 0);
				  lcd_send_string((char*)lcdBuf);
				  lcdToggle = 2;
			  }
			  else if (lcdToggle == 2)
			  {
				  sprintf(lcdBuf, "Km/h: %.02f", (gps_data.speed*1.852));
				  lcd_clear ();
				  lcd_put_cur(0, 0);
				  lcd_send_string((char*)lcdBuf);
				  sprintf(lcdBuf, "Heading: %.02f", gps_data.course);
				  lcd_put_cur(1, 0);
				  lcd_send_string((char*)lcdBuf);
				  lcdToggle = 3;
			  }
			  else if (lcdToggle == 3)
			  {
				  sprintf(lcdBuf, "Temp: %.02f DegC", bme280_data.temperature);
				  lcd_clear ();
				  lcd_put_cur(0, 0);
				  lcd_send_string((char*)lcdBuf);
				  sprintf(lcdBuf, "Pres: %.02f hPa", bme280_data.pressure);
				  lcd_put_cur(1, 0);
				  lcd_send_string((char*)lcdBuf);
				  lcdToggle = 4;
			  }
			  else if (lcdToggle == 4)
			  {
				  sprintf(lcdBuf, "Hum: %.02f %%RH", bme280_data.humidity);
				  lcd_clear ();
				  lcd_put_cur(0, 0);
				  lcd_send_string((char*)lcdBuf);
				  sprintf(lcdBuf, "WaterLvl: %s", waterlevel_str[water_level]);
				  lcd_put_cur(1, 0);
				  lcd_send_string((char*)lcdBuf);
				  lcdToggle = 0;
			  }
		  }
		  else
		  {
			  sprintf(lcdBuf, "NO GPS LOCK!");
			  lcd_clear ();
			  lcd_put_cur(0, 0);
			  lcd_send_string((char*)lcdBuf);
//			  sprintf(uartBuf, "NO GPS LOCK!\r\n\n");
//			  HAL_UART_Transmit(&huart2, (uint8_t*)uartBuf, strlen(uartBuf), 1000);
		  }
	  }
  }
  /* USER CODE END 3 */
}

